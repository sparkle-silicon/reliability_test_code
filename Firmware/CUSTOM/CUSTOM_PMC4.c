void Service_PCI5_Main(void)
{
    if(PMC4_STR & C_D4)
    {
        PM4Cmd = PMC4_DIR;
    #if ENABLE_DEBUGGER_SUPPORT
    /* Debugger record */
        Debugger_KBC_PMC_Record(0, 4, PM4Cmd);
    #endif
        dprint("PM4Cmd is %#x\n", PM4Cmd);
    #if !(LPC_WAY_OPTION_SWITCH)
        PMC4_CTL |= IBF_INT_ENABLE;
    #endif
        PM4Step = 0;
        (Port78_Table[PM4Cmd >> 4])();
    }
    else
    {
        PM4Data = PMC4_DIR;
    #if ENABLE_DEBUGGER_SUPPORT
    /* Debugger record */
        Debugger_KBC_PMC_Record(0, 4, PM4Data);
    #endif
        PM4Step = 0;
    #if !(LPC_WAY_OPTION_SWITCH)
        PMC4_CTL |= IBF_INT_ENABLE;
    #endif
        dprint("PM4Data is %#x\n", PM4Data);
        if(PM4Step != 0x00)
        {
            // Pls use PM4Step for Port 78 Routine Command
            (Port78_Table[PM4Cmd >> 4]());
        }
        else
        {
            PM4UnProcessCnt = 0;
            while(PM4UnProcessCnt < 0xFF)
            {
                PM4UnProcessCnt++;
            }
        }
    }
}
