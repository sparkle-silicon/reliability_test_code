// Set&Get Status for Next Port60 Data Handle
//----------------------------------------------------------------------------
BYTE Set_PortPM4_Data_Handle(void)
{
    if(PM4Step == 0x00)
    {
        PM4Step = 0x01; // Set Next Port60 Data Handle again
        return (0);
    }
    else
    {
        PM4Step = 0x00; // Set Command Finished
        return (1);
    }
}
void PMC4_Cmd_33(void)
{
    if(Set_PortPM4_Data_Handle()) // Get PM4Data
    {
        PMC4_DOR = 0x00;
    #if ENABLE_DEBUGGER_SUPPORT
        Debugger_KBC_PMC_Record(1, 4, 0);
    #endif
    }
}
