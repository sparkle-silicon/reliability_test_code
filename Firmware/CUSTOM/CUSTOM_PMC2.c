// Set&Get Status for Next Port60 Data Handle
//----------------------------------------------------------------------------
static BYTE Set_PortPM2_Data_Handle(void)
{
    if (PM2Step == 0x00)
    {
        PM2Step = 0x01; // Set Next Port60 Data Handle again
        return (0);
    }
    else
    {
        PM2Step = 0x00; // Set Command Finished
        return (1);
    }
}
void PMC2_Cmd_33(void)
{
    if (Set_PortPM2_Data_Handle()) // Get PM2Data
    {
        PMC2_DOR = 0x00;
    }
}
