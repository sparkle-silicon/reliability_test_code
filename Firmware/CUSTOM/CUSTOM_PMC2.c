void Service_PCI3_Main(void)
{
    VBYTE cnt = 0;
    if (PMC2_STR & C_D2)
    {
        PM2Cmd = PMC2_DIR;
#if ENABLE_DEBUGGER_SUPPORT
        /* Debuffer record */
        Debugger_KBC_PMC_Record(0, 2, PM2Cmd);
#endif
#if !(LPC_WAY_OPTION_SWITCH)
        PMC2_CTL |= IBF_INT_ENABLE;
#endif
        PM2Step = 0;
        (Port6C_Table[PM2Cmd >> 4])();
    }
    else
    {
        PM2Data = PMC2_DIR;
#if ENABLE_DEBUGGER_SUPPORT
        /* Debuffer record */
        Debugger_KBC_PMC_Record(0, 2, PM2Data);
#endif
#if !(LPC_WAY_OPTION_SWITCH)
        PMC2_CTL |= IBF_INT_ENABLE;
#endif
        if (PM2Data != 0x0)
            dprint("Enter else PM2Data is %#x, PMC2_STR is %#x \n", PM2Data, PMC2_STR);
        if (PM2Step != 0x00)
        {
            // Pls use PM2Step for Port 6C Routine Command
            (Port6C_Table[PM2Cmd >> 4]());
        }
        else
        {
            while (cnt < 0xFF)
            {
                cnt++;
            }
        }
    }
}
